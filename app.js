const Kelvin = 293; // a constant named kelvin with 293 value.
const Celsius = Kelvin - 273; // constant Celsius smaller with 273 then Kelvin.
let Fahrenheit = Celsius * (9/5) + 32; // a variable type that can change its value.
Fahrenheit = (Math.floor(Fahrenheit)); // a new value for variable Fahrenheit that round down its decimal number value.
console.log(`The temperature is ${Fahrenheit} degrees Fahrenheit.`);